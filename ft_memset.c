void	*ft_memset(void *s, int c, size_t n)
{
	int	i;

	i = 0;
	if (s[i] != '\0')
	{
		while (s[i] != '\0' && n != 0)
		{
			s[i] = c;
			i++;
			n--;
		}
	}
}
